struct text_s {
    GLuint prog;
    GLint  aspect_loc, color_loc, font_loc;
    GLuint tex;
    GLuint vao, vbo;
};

static char *read_text_file(const char *path) {
    FILE *f = fopen(path, "rb");
    if (!f) { fprintf(stderr, "text: failed to open %s\n", path); return NULL; }
    fseek(f, 0, SEEK_END);
    long sz = ftell(f);
    rewind(f);
    char *buf = malloc((size_t)sz + 1);
    if (!buf) { fclose(f); return NULL; }
    size_t r = fread(buf, 1, (size_t)sz, f);
    buf[r] = '\0';
    fclose(f);
    return buf;
}

static GLuint compile(GLenum type, const char *src) {
    GLuint s = glCreateShader(type);
    glShaderSource(s, 1, &src, NULL);
    glCompileShader(s);
    GLint ok = 0;
    glGetShaderiv(s, GL_COMPILE_STATUS, &ok);
    if (!ok) {
        char log[1024];
        glGetShaderInfoLog(s, sizeof log, NULL, log);
        fprintf(stderr, "text shader compile: %s\n", log);
        glDeleteShader(s);
        return 0;
    }
    return s;
}

static GLuint load_program(const char *vp, const char *fp) {
    char *vs = read_text_file(vp);
    char *fs = read_text_file(fp);
    if (!vs || !fs) { free(vs); free(fs); return 0; }
    GLuint v = compile(GL_VERTEX_SHADER, vs);
    GLuint f = compile(GL_FRAGMENT_SHADER, fs);
    free(vs); free(fs);
    if (!v || !f) return 0;
    GLuint p = glCreateProgram();
    glAttachShader(p, v); glAttachShader(p, f);
    glLinkProgram(p);
    GLint ok = 0;
    glGetProgramiv(p, GL_LINK_STATUS, &ok);
    glDeleteShader(v); glDeleteShader(f);
    if (!ok) {
        char log[1024];
        glGetProgramInfoLog(p, sizeof log, NULL, log);
        fprintf(stderr, "text program link: %s\n", log);
        glDeleteProgram(p);
        return 0;
    }
    return p;
}

static GLuint build_font_texture(void) {
    size_t bytes = (size_t)FONT_W * FONT_H * 4;
    unsigned char *px = calloc(1, bytes);
    if (!px) return 0;
    for (int idx = 0; idx < 96; idx++) {
        int col = idx % FONT_COLS;
        int row = idx / FONT_COLS;
        for (int gy = 0; gy < GLYPH_PX; gy++) {
            unsigned char bits = FONT8X8[idx][gy];
            for (int gx = 0; gx < GLYPH_PX; gx++) {
                if (!(bits & (1u << gx))) continue;
                int tx = col * GLYPH_CELL + GLYPH_PAD + gx;
                int ty = row * GLYPH_CELL + GLYPH_PAD + gy;
                size_t o = ((size_t)ty * FONT_W + (size_t)tx) * 4;
                px[o + 0] = 255; px[o + 1] = 255; px[o + 2] = 255; px[o + 3] = 255;
            }
        }
    }
    GLuint tex = 0;
    glGenTextures(1, &tex);
    glBindTexture(GL_TEXTURE_2D, tex);
    glTexImage2D(GL_TEXTURE_2D, 0, GL_RGBA, FONT_W, FONT_H, 0,
                 GL_RGBA, GL_UNSIGNED_BYTE, px);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
    glBindTexture(GL_TEXTURE_2D, 0);
    free(px);
    return tex;
}

text_t *text_create(void) {
    text_t *t = calloc(1, sizeof *t);
    if (!t) return NULL;

    t->prog = load_program("shaders/text.vert", "shaders/text.frag");
    if (!t->prog) { free(t); return NULL; }
    t->aspect_loc = glGetUniformLocation(t->prog, "u_aspect");
    t->color_loc  = glGetUniformLocation(t->prog, "u_color");
    t->font_loc   = glGetUniformLocation(t->prog, "u_font");

    t->tex = build_font_texture();
    if (!t->tex) { glDeleteProgram(t->prog); free(t); return NULL; }

    glGenVertexArrays(1, &t->vao);
    glGenBuffers(1, &t->vbo);
    glBindVertexArray(t->vao);
    glBindBuffer(GL_ARRAY_BUFFER, t->vbo);
    glBufferData(GL_ARRAY_BUFFER,
                 (GLsizeiptr)(TEXT_MAX_CHARS * 6 * 4 * sizeof(float)),
                 NULL, GL_DYNAMIC_DRAW);
    glVertexAttribPointer(0, 2, GL_FLOAT, GL_FALSE, 4 * sizeof(float), (void *)0);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 4 * sizeof(float), (void *)(2 * sizeof(float)));
    glEnableVertexAttribArray(1);
    glBindVertexArray(0);
    return t;
}

void text_destroy(text_t *t) {
    if (!t) return;
    glDeleteVertexArrays(1, &t->vao);
    glDeleteBuffers(1, &t->vbo);
    glDeleteTextures(1, &t->tex);
    glDeleteProgram(t->prog);
    free(t);
}

float text_width(const char *str, float scale) {
    return (float)strlen(str) * scale * GLYPH_ADVANCE;
}

float text_height(float scale) {
    return scale;
}

void text_draw(text_t *t, const char *str, float x, float y, float scale,
               float r, float g, float b, float a, float aspect) {
    int n = (int)strlen(str);
    if (n > TEXT_MAX_CHARS) n = TEXT_MAX_CHARS;

    static float buf[TEXT_MAX_CHARS * 6 * 4];
    int vi = 0;
    float adv = scale * GLYPH_ADVANCE;
    float cx = x;
    for (int i = 0; i < n; i++) {
        unsigned char ch = (unsigned char)str[i];
        if (ch < 0x20 || ch > 0x7F) { cx += adv; continue; }
        int idx = ch - 0x20;
        int col = idx % FONT_COLS;
        int row = idx / FONT_COLS;
        float u0 = (float)(col * GLYPH_CELL + GLYPH_PAD) / (float)FONT_W;
        float u1 = (float)(col * GLYPH_CELL + GLYPH_PAD + GLYPH_PX) / (float)FONT_W;
        float v0 = (float)(row * GLYPH_CELL + GLYPH_PAD) / (float)FONT_H;
        float v1 = (float)(row * GLYPH_CELL + GLYPH_PAD + GLYPH_PX) / (float)FONT_H;
        float x0 = cx, x1 = cx + scale, y0 = y, y1 = y + scale;
        float quad[24] = {
            x0, y0, u0, v0,  x1, y0, u1, v0,  x1, y1, u1, v1,
            x0, y0, u0, v0,  x1, y1, u1, v1,  x0, y1, u0, v1,
        };
        for (int k = 0; k < 24; k++) buf[vi++] = quad[k];
        cx += adv;
    }
    if (vi == 0) return;

    glDisable(GL_DEPTH_TEST);
    glUseProgram(t->prog);
    glUniform1f(t->aspect_loc, aspect);
    glUniform4f(t->color_loc, r, g, b, a);
    glActiveTexture(GL_TEXTURE1);
    glBindTexture(GL_TEXTURE_2D, t->tex);
    glUniform1i(t->font_loc, 1);
    glBindVertexArray(t->vao);
    glBindBuffer(GL_ARRAY_BUFFER, t->vbo);
    glBufferSubData(GL_ARRAY_BUFFER, 0, (GLsizeiptr)((size_t)vi * sizeof(float)), buf);
    glDrawArrays(GL_TRIANGLES, 0, vi / 4);
    glBindVertexArray(0);
    glActiveTexture(GL_TEXTURE0);
    glEnable(GL_DEPTH_TEST);
}